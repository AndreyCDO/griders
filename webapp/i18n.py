SUPPORTED_LANGS = {"ru", "en"}
LANG_COOKIE = "griders_lang"


def normalize_lang(value: str | None) -> str:
    if not value:
        return "ru"
    value = value.lower().split(",", 1)[0].split("-", 1)[0].strip()
    return value if value in SUPPORTED_LANGS else "ru"


